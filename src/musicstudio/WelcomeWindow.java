    private void bInvoiceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bInvoiceBtnActionPerformed
        double bookedDays = Double.parseDouble(bTotalDaysTxt.getText());
        double subTotal = Double.parseDouble(bSubTotalTxt.getText());
        double invTax = Double.parseDouble(bTaxTxt.getText());
        double bDiscount = Double.parseDouble(bDiscountTxt.getText());
        
        double bGrndTotal = (bookedDays * dailyRate) + invTax - bDiscount;
                
        String sTotal = String.format("%.2f", bGrndTotal);
        bTotalTxt.setText(sTotal);
        
        bInvTxtArea.append("\n\t M-STUDIO INVOICE\n\n" +
                
                "  No. of days: \t" + bookedDays +
                "\n  Daily Rate: \t" + dailyRate +
                "\n  Sub-Total: \t" + subTotal + 
                "\n  Discount: \t" + bDiscount +
                "\n  Tax: \t" + invTax +
                "\n\n  Total: \t" + bGrndTotal +
                "\n\n\t Thanks for patronising us");
    }//GEN-LAST:event_bInvoiceBtnActionPerformed

    private void bTotalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTotalBtnActionPerformed
        double bTotalDays = Double.parseDouble(bTotalDaysTxt.getText());
        
        double bTotalDiscount = Double.parseDouble(bDiscountTxt.getText());
        double bTotalTax = Double.parseDouble(bTaxTxt.getText());
        
        double subTotal = (bTotalDays * dailyRate);
        
        double bTotal = (subTotal - bTotalDiscount) + bTotalTax;
        
        
                
        String sSubTotal = String.format("%.2f", subTotal);
        bSubTotalTxt.setText(sSubTotal);
        
        String sTotal = String.format("%.2f", bTotal);
        bTotalTxt.setText(sTotal);
        
    }//GEN-LAST:event_bTotalBtnActionPerformed

